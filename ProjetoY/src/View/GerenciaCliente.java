    @SuppressWarnings("unchecked")
    public void carregaTabela() {

        DefaultTableModel modelo = (DefaultTableModel) this.jTableClientes.getModel();
        modelo.setNumRows(0);

        ArrayList<Cliente> minhalista = new ArrayList<>();
        minhalista = objcliente.getMinhaLista();

        for (Cliente a : minhalista) {
            modelo.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getIdade(),
                a.getTelefone(),
                a.getCpf()
            });
        }
    }

